<script type="text/javascript">
    $(document).ready(function(){
        var CSRF_TOKEN = $('meta[name="csrf-token"]').attr('content');
        getSpecialtiesData();
        getAboutData();
        $("#send-btn-contact").click(function(){
            if (!$("#name").val()) {
                $("#errorName").html('الرجاء ادخال الاسم');
                $("#errorName").show();
            }else{
                $("#errorName").hide();
            }
            if (!$("#phone").val()) {
                $("#errorPhone").html('الرجاء ادخال رقم الجوال');
                $("#errorPhone").show();
            }else{
                $("#errorPhone").hide();
            }
            if (!$("#text").val()) {
                $("#errorText").html('الرجاء ادخال ملخص الاستشاره');
                $("#errorText").show();
            }else{
                $("#errorText").hide();
            }



            if($("#name").val() && $("#phone").val() && $("#text").val()){
                $.ajax({
                    /* the route pointing to the post function */
                    url: 'https://law.apis.dokan.notjustapage.com/api/site/contact',
                    type: 'POST',
                    /* send the csrf-token and the input to the controller */
                    data: {
                        _token: CSRF_TOKEN,
                        name:   $("#name").val(),
                        email:  $("#email").val(),
                        phone:  $("#phone").val(),
                        text:   $("#text").val()
                    },
                    dataType: 'JSON',
                    /* remind that 'data' is the response of the AjaxController */
                    success: function (response) {
                        $('#successMessage').html(' تم الارسال بنجاح  ');
                        $("#name").val('')
                        $("#phone").val('')
                        $("#text").val('')
                        $("#email").val('')

                    },
                    error : function(data){
                        var result = '';
                        for (var i in data.error) {
                            // obj.hasOwnProperty() is used to filter out properties from the object's prototype chain
                            if (data.error.hasOwnProperty(i)) {
                                result += i + "-" +  data.error[i] + '<br>';
                            }
                        }
                        $("#info").html(result);
                        $("#info").show();
                    }


                });
            }


        });
    });


    function getSpecialtiesData(){
        $.ajax({
            url: 'https://law.apis.dokan.notjustapage.com/api/site/specialties',
            type: 'get',
            dataType: 'JSON',
            success: function (response) {
                var result = '';
                for (var item in response.data) {
                    result += '<a href="{{route("special")}}">';
                    result += '<div class="ourservice-panel animated wow fadeInUp" style="width: 290px;margin-top: 20px;margin-right:0px" data-wow-delay="100ms"   >';
                    result += '<div class="ourservice-icon" style="padding: 0px;"> <img src="'+ response.data[item].image +'" style="height: 195px;" alt="insurance">  <div class="ourservice-content"></div></div>';
                    result += '<h3 style="background-color: #fff;padding: 30px 30px 25px 30px;">'+ response.data[item].subject +'</h3>';
                    result += '</div>';
                    result += '</a>';
                }
                $('.ourservice-slider').html(result)
            }
        });
    }

    function getAboutData(){
        $.ajax({
            /* the route pointing to the post function */
            url: 'https://law.apis.dokan.notjustapage.com/api/site/about',
            type: 'get',
            /* send the csrf-token and the input to the controller */
            // data: {_token: CSRF_TOKEN, name:$("#name").val(),date:$("#date").val(),time:$("#time").val()},
            dataType: 'JSON',
            /* remind that 'data' is the response of the AjaxController */
            success: function (data) {
                console.log(data.data.text)
                // console.log(data.aboute[0].name);
                $('.fullwidth-gapper').html('<p>'+data.data.text+'</p>')
            }
        });
    }



